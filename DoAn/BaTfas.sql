CREATE DATABASE ThreeTfas
GO
USE ThreeTfas
GO
CREATE TABLE ChucDanh(
	MaCD INT PRIMARY KEY,
	TenCD NVARCHAR(50),
	LuongCB FLOAT,
	TrangThai VARCHAR(20)
)
GO
CREATE TABLE CuaHang(
	MaCH INT PRIMARY KEY,
	TenCH NVARCHAR(50),
	DiaChi NVARCHAR(100),
	HinhAnh VARCHAR(100),
	TrangThai VARCHAR(20)
)
GO
CREATE TABLE NhanVien(
	MaNV INT PRIMARY KEY,
	Ho NVARCHAR(10),
	TenLot NVARCHAR(10),
	Ten NVARCHAR(30),
	NgaySinh DATE,
	Phai NVARCHAR(3),
	CCCD VARCHAR(12),
	SDT VARCHAR(10),
	DiaChi NVARCHAR(100),
	NgayBDLam DATE,
	NgayKetThuc DATE,
	MaCD INT,
	MaCH INT,
	MaNV_QL INT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaCD) REFERENCES ChucDanh (MaCD),
	FOREIGN KEY(MaCH) REFERENCES CuaHang (MaCH),
	FOREIGN KEY(MaNV_QL) REFERENCES NhanVien(MaNV)
)
GO
CREATE TABLE ChamCong(
	MaCC INT PRIMARY KEY,
	NgayCham DATE,
	MaNV INT,
	MaNV_CC INT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaNV) REFERENCES NhanVien (MaNV),
	FOREIGN KEY(MaNV_CC) REFERENCES NhanVien (MaNV)
)
GO
CREATE TABLE LoaiCham(
	MaLC INT PRIMARY KEY,
	TenLC NVARCHAR(20),
	HinhThuc NVARCHAR(10),
	SoTien FLOAT,
)
GO
CREATE TABLE CT_ChamCong(
	MaCC INT,
	MaLC INT,
	PRIMARY KEY(MaCC,MaLC),
	FOREIGN KEY(MaLC) REFERENCES LoaiCham (MaLC),
	FOREIGN KEY(MaCC) REFERENCES ChamCong (MaCC)
)
GO
CREATE TABLE Luong(
	MaNV INT,
	Thang INT,
	Nam INT,
	Luong FLOAT,
	TrangThai VARCHAR(20),
	PRIMARY KEY(MaNV,Thang,Nam),
	FOREIGN KEY(MaNV) REFERENCES NhanVien (MaNV)
)
GO
CREATE TABLE LoaiKH(
	MaLoai INT PRIMARY KEY,
	TenLoai NVARCHAR(50),
	HanMuc FLOAT ,
	TrangThai VARCHAR(20)
)
GO
CREATE TABLE KhachHang(
	MaKH INT PRIMARY KEY,
	TenKH NVARCHAR(50),
	NgaySinh DATE,
	Phai NVARCHAR(3),
	SDT VARCHAR(10),
	DiaChi VARCHAR(100),
	DoanhSo float,
	MaLoai INT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaLoai) REFERENCES LoaiKH (MaLoai)
)
GO
CREATE TABLE Mau(
	MaMau INT PRIMARY KEY,
	TenMau NVARCHAR(20),
	TrangThai VARCHAR(20)
)

GO
CREATE TABLE Size(
	MaSize INT PRIMARY KEY,
	TenSize NVARCHAR(20),
	TrangThai VARCHAR(20)
)

GO
CREATE TABLE LoaiSP(
	MaLoai INT PRIMARY KEY,
	TenLoai NVARCHAR(50),
	TrangThai VARCHAR(20)
)
GO
CREATE TABLE SanPham(
	MaSP INT ,
	TenSP NVARCHAR(50),
	MaMau INT,
	MaSize INT,
	MaLoai INT,
	Gia FLOAT,
	TrangThai VARCHAR(20),
	PRIMARY KEY(MaSP,MaMau,MaSize),
	FOREIGN KEY(MaMau) REFERENCES Mau (MaMau),
	FOREIGN KEY(MaSize) REFERENCES Size (MaSize),
	FOREIGN KEY(MaLoai) REFERENCES LoaiSP (MaLoai),
)
GO
CREATE TABLE NhaCungCap(
	MaNCC INT PRIMARY KEY,
	TenNCC NVARCHAR(50),
	DiaChi NVARCHAR(100),
	Email VARCHAR(50),
	SDT VARCHAR(10),
	TrangThai VARCHAR(20)
)
GO
CREATE TABLE HoaDonNhap(
	MaHD INT PRIMARY KEY,
	NgayLap DATE,
	MaNCC INT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaNCC) REFERENCES NhaCungCap (MaNCC),
)
GO
CREATE TABLE CT_HoaDonNhap(
	MaHD INT,
	MaSP INT,
	MaMau INT,
	MaSize INT,
	Gia FLOAT,
	SoLuong INT,
	PRIMARY KEY(MaHD,MaSP,MaMau,MaSize),
	FOREIGN KEY(MaHD) REFERENCES HoaDonNhap (MaHD),
	FOREIGN KEY(MaSP,MaMau,MaSize) REFERENCES SanPham (MaSP,MaMau,MaSize),
)
GO
CREATE TABLE SP_PhanBo(
	MaSP INT,
	MaMau INT,
	MaSize INT,
	MaCH INT,
	SoLuong INT,
	TrangThai VARCHAR(20),
	PRIMARY KEY(MaSP,MaMau,MaSize,MaCH),
	FOREIGN KEY(MaSP,MaMau,MaSize) REFERENCES SanPham (MaSP,MaMau,MaSize),
	FOREIGN KEY(MaCH) REFERENCES CuaHang (MaCH),
)
GO
CREATE TABLE TaiKhoan(
	username VARCHAR(50) PRIMARY KEY,
	password VARCHAR(50),
	MaNV INT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaNV) REFERENCES NhanVien (MaNV),
)
GO
CREATE TABLE DonHang(
	MaDH INT PRIMARY KEY,
	MaNV INT,
	MaKH INT,
	NgayLap DATE,
	NgayNhan DATE,
	ChietKhau FLOAT,
	GhiChu NVARCHAR(200),
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaKH) REFERENCES KhachHang (MaKH),
	FOREIGN KEY (MaNV) REFERENCES NhanVien (MaNV)
)
GO
CREATE TABLE CT_DonHang(
	MaDH INT,
	MaSP INT,
	MaMau INT,
	MaSize INT,
	Gia FLOAT,
	SoLuong INT,
	PRIMARY KEY(MaDH,MaSP,MaMau,MaSize),
	FOREIGN KEY(MaDH) REFERENCES DonHang (MaDH),
	FOREIGN KEY(MaSP,MaMau,MaSize) REFERENCES SanPham (MaSP,MaMau,MaSize),
)
GO
CREATE TABLE HoaDon(
	MaHD INT PRIMARY KEY,
	MaNV INT,
	MaKH INT,
	NgayLap DATE,
	ChietKhau FLOAT,
	TrangThai VARCHAR(20),
	FOREIGN KEY(MaKH) REFERENCES KhachHang (MaKH),
	FOREIGN KEY(MaNV) REFERENCES NhanVien (MaNV),
)
GO
CREATE TABLE CT_HoaDon(
	MaHD INT,
	MaSP INT,
	MaMau INT,
	MaSize INT,
	Gia FLOAT,
	SoLuong INT,
	PRIMARY KEY(MaHD,MaSP,MaMau,MaSize),
	FOREIGN KEY(MaHD) REFERENCES HoaDon (MaHD),
	FOREIGN KEY(MaSP,MaMau,MaSize) REFERENCES SanPham (MaSP,MaMau,MaSize),
)
GO
CREATE TABLE KhoHang(
MaSP INT,
MaMau INT,
MaSize INT,
SoLuong INT,
PRIMARY KEY (MaSP,MaMau,MaSize),
FOREIGN KEY(MaSP,MaMau,MaSize) REFERENCES SanPham (MaSP,MaMau,MaSize),
)
GO
